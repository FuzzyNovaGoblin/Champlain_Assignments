def mult(arg1=1, arg2=1):
    if arg1 == None:
        return 0
    return arg1*arg2


def div(arg1=1, arg2=1):
    if arg1 == None:
        return 0
    return arg1/arg2


def add(arg1, arg2=1):
    if arg1 == None:
        return 0
    return arg1+arg2


def sub(arg1, arg2=0):
    if arg1 == None:
        return 0
    return arg1-arg2
